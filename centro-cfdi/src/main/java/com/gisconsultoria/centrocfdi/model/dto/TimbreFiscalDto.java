package com.gisconsultoria.centrocfdi.model.dto;

public class TimbreFiscalDto {

	private String fechaTimbrado;
	
	private String timeTimbre;
	
	private String uuid;
	
	private String certificadoSat;
	
	private String version;
	
	private String selloCfd;
	
	private String selloSat;

	public String getFechaTimbrado() {
		return fechaTimbrado;
	}

	public void setFechaTimbrado(String fechaTimbrado) {
		this.fechaTimbrado = fechaTimbrado;
	}

	public String getTimeTimbre() {
		return timeTimbre;
	}

	public void setTimeTimbre(String timeTimbre) {
		this.timeTimbre = timeTimbre;
	}

	public String getUuid() {
		return uuid;
	}

	public void setUuid(String uuid) {
		this.uuid = uuid;
	}

	public String getCertificadoSat() {
		return certificadoSat;
	}

	public void setCertificadoSat(String certificadoSat) {
		this.certificadoSat = certificadoSat;
	}

	public String getVersion() {
		return version;
	}

	public void setVersion(String version) {
		this.version = version;
	}

	public String getSelloCfd() {
		return selloCfd;
	}

	public void setSelloCfd(String selloCfd) {
		this.selloCfd = selloCfd;
	}

	public String getSelloSat() {
		return selloSat;
	}

	public void setSelloSat(String selloSat) {
		this.selloSat = selloSat;
	}
	
}
